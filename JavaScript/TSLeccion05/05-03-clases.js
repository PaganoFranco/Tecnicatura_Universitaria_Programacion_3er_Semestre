class Persona{ 
    //Creamos un constructor
    constructor(nombre, apellido){
        this.nombre = nombre;
        this.apellido = apellido;
    }
}

//Creamos objetos
let persona1 = new Persona("Franco", "Pagano");
console.log(persona1);
let persona2 = new Persona("Julieta", "Quiroga");
console.log(persona2);
